    <h3>REFUSER LES EMBALLAGES SUPERFLUS</h3>
    <p>
    Le premier des 5 R de la démarche zéro déchet est 
    <strong>« refuser »</strong> et pour cause : c'est celui 
    qui permet de réduire le plus rapidement ses déchets. 
    Par exemple, à la boulangerie, les baguettes sont désormais 
    systématiquement données dans un sac en papier : n'hésitez pas à préciser 
    que vous n'en voulez pas. Si vous souhaitez les transporter emballées, 
    un sac type « tote bag » est tout aussi pratique. Il permet aussi 
    de transporter des viennoiseries...
    </p>
    <p>
    Vous avez peur de demander ? Essayez ! Vous verrez que 
    ce geste simple n'est pas mal vu et apporte beaucoup de satisfaction !
    </p>
    <h3>FAIRE SES COURSES...</h3>
    <p>Oui, ça demande un peu d'organisation... mais une fois 
        les habitudes prises, c'est un plaisir de voir ces jolis 
        bocaux décorer la cuisine et la poubelle jaune diminuer !
    </p>
    <ul>
        <li>
            <strong>Faire une liste de courses et s'y tenir</strong> permet d'éviter 
            les achats compulsifs et de prévoir les contenants nécessaires.
            <div class="encart">
                <p>
                Selon l'ADEME (Agence de la transition écologique), 10 millions de tonnes de 
                nourriture sont jetées chaque année en France, soit 400 € par an 
                pour une famille de 4 personnes
                </p> 
            </div>
        </li>
        <li>
            <strong>Planifier vos repas</strong> permet de n'acheter que 
            ce dont on a besoin (donc économiser) et de ne pas gâcher de nourriture.
            <div class="encart">
                <p>Pensez à ranger votre frigo pour optimiser 
                    la conservation des aliments. Rendez-vous sur
                    <a href="https://agirpourlatransition.ademe.fr/particuliers/conso/alimentation/chaine-froid-dates-peremption-quil-faut-savoir">https://agirpourlatransition.ademe.fr/particuliers/conso/alimentation/chaine-froid-dates-peremption-quil-faut-savoir</a>
                </p>
            </div>	
        </li>
        <li>
            <strong>Acheter en vrac et à la coupe</strong>. Pas d'emballages, pas de déchets ! 
            Ça permet aussi de garder la main sur les quantités 
            et donc d'éviter le gaspillage. Il suffit d'avoir une 
            série contenants (voir encart kit de vrac) et une liste de 
            commerçants qui proposent des produits en vrac. Pour avoir quelques 
            adresses, rendez-vous sur notre carte (lien vers la page) 
            et n'hésitez pas à partager vos bons plans !
        </li>
        <li>
            Choisir des <strong>produits de saison</strong>, 
            <strong>locaux</strong> et/ou <strong>labellisés</strong> qui sont souvent moins emballés 
            (car moins transportés) et plus goûteux. En plus, ça favorise 
            le tissu économique et social local.
        </li>
    </ul>
    <h3>PAR EXEMPLE, REMPLACER...</h3>
    <ul>
        <li>… les <strong>éponges</strong> (à la durée de vie courte et non recyclables)...
            <ul>
                <li>
                … par un <strong>« tawashi »</strong> : c'est une éponge japonaise 
                faite de tissus récupérés. Pratique pour nettoyer la table par exemple.
                </li>
                <li>
                En attendant l'atelier, voici un tuto permettant de le faire avec des pinces à linge <a href="https://www.aufeminin.com/deco-pro/eponge-tawashi-s4009554.html">https://www.aufeminin.com/deco-pro/eponge-tawashi-s4009554.html</a>

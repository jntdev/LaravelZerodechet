    <section class="superflus_cuisine">
    <h3>REFUSER LES EMBALLAGES SUPERFLUS</h3>
    <p>
        Le premier des 5 R de la démarche zéro déchet est 
        <strong>« refuser »</strong> et pour cause : c'est celui 
        qui permet de réduire le plus rapidement ses déchets. 
        Par exemple, à la boulangerie, les baguettes sont désormais 
        systématiquement données dans un sac en papier : n'hésitez pas à préciser 
        que vous n'en voulez pas. Si vous souhaitez les transporter emballées, 
        un sac type « tote bag » est tout aussi pratique. Il permet aussi 
        de transporter des viennoiseries...
    </p>
    <p>
        Vous avez peur de demander ? Essayez ! Vous verrez que 
        ce geste simple n'est pas mal vu et apporte beaucoup de satisfaction !
    </p>
    </section>
    

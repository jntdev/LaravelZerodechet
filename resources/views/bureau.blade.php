            <section class="stop_pub">
                <h3>STOP PUB!</h3>
                <p>Parmi les papiers, les publicités qui inondent nos boîtes aux lettres sont particulièrement problématiques 
                    (papier mais aussi ressources énergétiques, encres et produits chimiques divers...). Chaque année, un foyer français 
                    reçoit environ 40kg de courrier non adressé, composé majoritairement de publicités.
                </p>
                <p>Coller l'autocollant « STOP PUB » sur sa boîte aux lettres est donc un moyen de limiter cet impact environnemental, 
                    tout en limitant les tentations de consommation... De plus, depuis le 1er janvier 2021, vous distribuer des imprimés non 
                    adressés alors que vous avez un autocollant « STOP PUB » sur votre boîte aux lettres est interdit.
                </p>
                <p>
                Vous pouvez vous procurer votre autocollant STOP PUB auprès de de la Mairie de Paimpol ou du SMITRED :

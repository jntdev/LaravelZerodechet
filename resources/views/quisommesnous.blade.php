            <p>Anne-Laure et Cédric voulaient se lancer dans un défi zéro déchet. 
                Ils rencontrèrent Fanny, qui avait envie de s'engager dans cette démarche, 
                grâce à Annette, qui l'avait déjà mise en relation avec Marie-Noëlle. 
                Puis, ils firent la connaissance de Lauriane, Christophe, Antoine, 
                Julian, Jonathan... et tous les autres, qui voulaient y participer.</p>
            <p>Chacun, tel un colibri, a pris part au projet à titre individuel. 
                A grands renforts de « visio » mais aussi de déj', de stands, 
                de verres en terrasse et de toute occasion d'échanger, nous en 
                sommes arrivés au défi. Le premier acte était donc de monter 
                ensemble un projet pour faire quelque chose pour l'environnement, 
                à l'échelle de notre territoire du Goëlo. L'association 
                Osez zéro déchet ! est donc née à Paimpol, en juin 2021, 
                pour promouvoir la démarche « zéro waste » 
                (zéro gaspillage et zéro déchet). Le deuxième acte est désormais 
                de partager ce projet avec le plus grand nombre, grâce aux 
                découvertes, aux rencontres et au partage, raisons d'être du défi.</p>
            <p>De plus en plus d'initiatives naissent : elles en feront sûrement un grand fleuve !</p>
            <p>Bonne visite !</p>
